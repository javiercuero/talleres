'''
Ingresar por teclado y por separado en dos variables, sus nombres y apellidos respectivamente,
mostrar en orden contrario al que se ingresaron, es decir apellidos y nombres
'''
# Definicio de variables y captura de datos
nombres = input("Ingrese sus nombres: ")                    # Se capturan los nombres del usuario.
apellidos = input("Ingrese sus apellidos: ")                # Se capturan los apellidos del usuario.

# Salida de datos
print("Sus apellidos y nombres son: ", apellidos, nombres)  # Se imprimen los apellidos y nombres del usuario.